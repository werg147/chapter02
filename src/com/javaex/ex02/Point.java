package com.javaex.ex02;

public class Point {

	private int x;
	private int y;
	
	//x값 입력
	public void setXval (int xv) {
		x = xv;
	}
	
	//x값 읽기
	public int getXval() {
		return x;
	}
	
	//y값 입력
	public void setYval (int yv) {
		y = yv;
	}
	
	//y값 읽기
	public int getYval() {
		return y;
	}
	
}
